cat /etc/passwd | grep -v  "\#" | sed -n 'n;p' | cut -d':' -f1 | rev 
| sort -rn | sed -n "$FT_LINE1, $FT_LINE2"p | paste -s -d"," 
| sed 's/,/, /g' | sed 's/$/./' | tr -d '\n' 